let resultado = "lista";

switch (resultado) {
    case "aprovada":
        console.log("Parabéns!");
    break;

    case "lista":
        console.log("Tenha fé! Você está na lista");
    break;

    case "reprovada":
        console.log("Não foi desta vez.");
    break
    default:
        console.log("Valor não identificado")
}